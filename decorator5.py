def dedefis(func):
    def wrapper(*args):
        s = func(*args)
        while '--' in s:
            s = s.replace('--', '-')
        return s

    return wrapper

@dedefis   
def kirilitsa(s):
    t = {'ё': 'yo', 'а': 'a', 'б': 'b', 'в': 'v', 'г': 'g', 'д': 'd', 'е': 'e', 'ж': 'zh',
     'з': 'z', 'и': 'i', 'й': 'y', 'к': 'k', 'л': 'l', 'м': 'm', 'н': 'n', 'о': 'o', 'п': 'p',
     'р': 'r', 'с': 's', 'т': 't', 'у': 'u', 'ф': 'f', 'х': 'h', 'ц': 'c', 'ч': 'ch', 'ш': 'sh',
     'щ': 'shch', 'ъ': '', 'ы': 'y', 'ь': '', 'э': 'e', 'ю': 'yu', 'я': 'ya'}
    return ''.join(['-' if j in ": ;.,_" else j for j in [t[i] if i in t else i for i in list(s.lower())]])

print(kirilitsa(input()))
